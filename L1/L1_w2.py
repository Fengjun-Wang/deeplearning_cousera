    for i in range(A.shape[1]):
        
        # Convert probabilities A[0,i] to actual predictions p[0,i]
        ### START CODE HERE ### (˜ 4 lines of code)
        value = sigmoid(A[0][i])
        Y_prediction[0][i] = 0 if value <= 0.5 else 1
